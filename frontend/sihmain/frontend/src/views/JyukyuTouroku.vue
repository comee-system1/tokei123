      riyosyaid: '',
      // タブの制御Flag
      JyukyuSyogaiFukusiFlag: false, // JyukyuSyogaiFukusiFlagの初期表示状態
      JyukyuSyogaiJiFlag: false, // JyukyuSyogaiJiFlagの初期表示状態
