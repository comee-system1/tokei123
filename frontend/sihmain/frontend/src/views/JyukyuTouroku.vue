      tabmenus: [
        { href: '#JyukyuSyogaiFukusi', text: '障害福祉' },
        { href: '#JyukyuSyogaiJi', text: '障害児' },
        { href: '#JyukyuChiikiSoudan', text: '地域相談支援' },
      ],
      tabmargin: '4px',
      tabheight: '30',
      menuitems: [
        {
          id: 1,
